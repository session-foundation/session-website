const lucideIconValues: Set<string> = new Set(Object.values(LUCIDE_ICONS_UNICODE));

export function isLucideIcon(unicode: string): unicode is LUCIDE_ICONS_UNICODE {
  return lucideIconValues.has(unicode);
}
